import java.util.Scanner;
class Odd{
    static Scanner sc=new Scanner(System.in);
    public static void main(String[]args)
    {
        System.out.print("enter the n value:");
        int n=sc.nextInt();
        int number=1;
        while(n<=n)
        {
            System.out.println(number+"");
            n--;
        }
    }
}